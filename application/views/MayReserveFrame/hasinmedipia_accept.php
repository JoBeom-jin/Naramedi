<p><strong>7. </strong><strong>정보 보호책임자&nbsp;및 담당자</strong><br>
①&nbsp;OK검진은&nbsp;개인정보 처리에 관한 업무를 총괄해서 책임지고, 개인정보 처리와 관련한 정보주체의 불만처리 및 피해구제 등을 위하여 아래와 같이 개인정보 보호책임자&nbsp;및 실무담당자를 지정하고 있습니다.</p>

