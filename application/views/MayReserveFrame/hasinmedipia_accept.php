<p>② 정보주체께서는&nbsp;OK검진의 서비스를&nbsp;이용하시면서 발생한 모든 개인정보 보호 관련 문의, 불만처리, 피해구제 등에 관한 사항을 개인정보 보호책임자 및 담당부서로 문의하실 수 있습니다.&nbsp;OK검진은&nbsp;정보주체의 문의에 대해 지체 없이 답변 및 처리해드릴 것입니다.<br>
