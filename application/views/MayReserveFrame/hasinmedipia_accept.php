                               <div><p>(주)인피니티케어('http://www.okaymedi.com/'이하 'OK검진')은(는) 개인정보보호법에 따라 이용자의 개인정보 보호 및 권익을 보호하고 개인정보와 관련한 이용자의 고충을 원활하게 처리할 수 있도록 다음과 같은 처리방침을 두고 있습니다.&nbsp;인피니티케어는 개인정보처리방침을 개정하는 경우 웹사이트 공지사항(또는 개별공지)을 통하여 공지할 것입니다.<br>
<br>
<strong>1. </strong><strong>개인정보의 처리 목적&nbsp;</strong></p>

<p>OK검진은&nbsp;개인정보를 다음의 목적을 위해 처리합니다. 처리한 개인정보는 다음의 목적&nbsp;이외의 용도로는 사용되지 않으며 이용 목적이 변경될 시에는 사전동의를 구할 예정입니다.</p>

<p>&nbsp;</p>

<p>&nbsp; &nbsp;가. 민원사무 처리</p>

<p>&nbsp;&nbsp; &nbsp;민원인의 신원 확인, 민원사항 확인, 사실조사를 위한 연락·통지, 처리결과 통보 등을 목적으로 개인정보를 처리합니다.</p>

<p>&nbsp; &nbsp;나. 재화 또는 서비스 제공<br>
&nbsp; &nbsp;서비스 제공 등을 목적으로 개인정보를 처리합니다.</p>

<p>&nbsp; &nbsp;다. 마케팅 및 광고에의 활용<br>
&nbsp; &nbsp;이벤트 및 광고성 정보 제공 및 참여기회 제공 등을 목적으로 개인정보를 처리합니다.<br>
<br>
<strong>2. </strong><strong>개인정보의 처리 및 보유 기간</strong></p>

<p>OK검진은 법령에 따른 개인정보 보유·이용기간 또는 정보주체로부터 개인정보를 수집시에 동의받은 개인정보 보유·이용기간 내에서 개인정보를 처리·보유합니다.</p>

<p>&nbsp;</p>

<p>①&nbsp;OK검진은 수집한 개인정보를 다음의 목적을 위해 활용합니다.</p>

<p>&nbsp;&nbsp; &nbsp;- 건강검진 예약&nbsp;서비스 이용에 따른 본인 확인, 예약 의사 확인, 연락처 확인, 고지사항 전달</p>

<p>&nbsp;</p>

<p>② OK검진은&nbsp;다음의 개인정보 항목을 처리하고 있습니다.<br>
&nbsp; &nbsp;- 필수항목 :&nbsp;이름, 휴대전화&nbsp;번호<br>
&nbsp; &nbsp;- 선택항목 : 성별, 이메일</p>

<p>&nbsp; &nbsp;- 수집방법 : 홈페이지</p>

<p>&nbsp;</p>

<p>③&nbsp;각각의 개인정보 처리 및 보유 기간은 다음과 같습니다.</p>

<p>&nbsp; &nbsp;가. 계약 또는 청약철회 등에 관한 기록</p>

<p>&nbsp; &nbsp; &nbsp; &nbsp;-보존 이유 : 전자상거래 등에서의 소비자보호에 관한 법률</p>

<p>&nbsp; &nbsp; &nbsp; &nbsp;-보존 기간 : 5년</p>

<p>&nbsp; &nbsp;나. 대금결제 및 재화 등의 공급에 관한 기록</p>

<p>&nbsp; &nbsp; &nbsp; &nbsp;-보존 이유 : 전자상거래 등에서의 소비자보호에 관한 법률</p>

<p>&nbsp; &nbsp; &nbsp; &nbsp;-보존 기간 : 5년</p>

<p>&nbsp; &nbsp;다. 소비자의 불만 또는 분쟁처리에 관한 기록</p>

<p>&nbsp; &nbsp; &nbsp; &nbsp;-보존 이유 : 전자상거래 등에서의 소비자보호에 관한 법률</p>

<p>&nbsp; &nbsp; &nbsp; &nbsp;-보존 기간 : 3년</p>

<p><br>
<strong>3. </strong><strong>정보주체의 권리,&nbsp;의무 및 그 행사방법</strong></p>

<p>① 정보주체는&nbsp;OK검진에&nbsp;대해 언제든지 다음 각 호의 개인정보 보호 관련 권리를 행사할 수 있습니다.<br>
&nbsp; &nbsp;-&nbsp;개인정보 열람요구<br>
&nbsp; &nbsp;-&nbsp;오류 등이 있을 경우 정정 요구<br>
&nbsp; &nbsp;-&nbsp;삭제요구<br>
&nbsp; &nbsp;- 처리정지 요구</p>

<p>&nbsp;</p>

<p>② 제1항에 따른 권리 행사는&nbsp;OK검진에 대해 개인정보 보호법 시행규칙 별지 제8호 서식에 따라 서면, 전자우편, 모사전송(FAX) 등을 통하여 하실 수 있으며 인피니티케어는 이에 대해 지체 없이 조치하겠습니다.</p>

<p>&nbsp;</p>

<p>③ 정보주체가 개인정보의 오류 등에 대한 정정 또는 삭제를 요구한 경우에는&nbsp;OK검진은&nbsp;정정 또는 삭제를 완료할 때까지 당해 개인정보를 이용하거나 제공하지 않습니다.</p>

<p>&nbsp;</p>

<p>④ 제1항에 따른 권리 행사는 정보주체의 법정대리인이나 위임을 받은 자 등 대리인을 통하여 하실 수 있습니다. 이 경우 개인정보 보호법 시행규칙 별지 제11호 서식에 따른 위임장을 제출하셔야 합니다.<br>
<br>
<strong>4. </strong><strong>개인정보의 파기</strong></p>

<p>OK검진은&nbsp;원칙적으로 개인정보 처리목적이 달성된 경우에는 지체없이 해당 개인정보를 파기합니다. 파기의 절차, 기한 및 방법은 다음과 같습니다.</p>

<p>&nbsp;</p>

<p>①&nbsp;파기절차<br>
이용자가 입력한 정보는 목적 달성 후 별도의 DB에 옮겨져(종이의 경우 별도의 서류) 내부 방침 및 기타 관련 법령에 따라 일정기간 저장된 후 혹은 즉시 파기됩니다. 이 때, DB로 옮겨진 개인정보는 법률에 의한 경우가 아니고서는 다른 목적으로 이용되지 않습니다.</p>

<p>&nbsp;</p>

<p>②&nbsp;파기기한<br>
이용자의 개인정보는 개인정보의 보유기간이 경과된 경우에는 보유기간의 종료일로부터 5일 이내에, 개인정보의 처리 목적 달성, 해당 서비스의 폐지, 사업의 종료 등 그 개인정보가 불필요하게 되었을 때에는 개인정보의 처리가 불필요한 것으로 인정되는 날로부터 5일 이내에 그 개인정보를 파기합니다.</p>

<p><strong>&nbsp;</strong></p>

<p><strong>5.&nbsp;</strong><strong>수집한 개인정보의 위탁</strong></p>

<p>OK검진은&nbsp;정보주체의 동의 없이 개인정보를 외부 업체에 위탁하지 않습니다. 향후 그러한 필요가 생길 경우 위탁 대상자와 위탁업무 내용에 대해 정보주체에 통지하고 필요한 경우 사전 동의를 받도록 하겠습니다.</p>

<p>&nbsp;</p>

<p><strong>6. </strong><strong>개인정보관련 의견수렴 및 침해, 불만처리에 관한 사항</strong></p>

<p>① 회사는 개인정보보호와 관련하여 이용자들의 의견을 수렴하고 있으며 불만을 처리하기 위하여 모든 절차와 방법을 마련하고 있습니다. 이용자들은 하단에 명시한 개인정보 보호책임자 및 담당자” 항을 참고하여 전화나 메일을 통하여 불만사항을 신고할 수 있고, 회사는 이용자들의 신고사항에 대하여 신속하게 처리하여 답변해 드립니다.<br>
② 또는 정부에서 설치하여 운영 중인 아래의 기관에 불만처리를 신청할 수 있습니다.<br>
