1. 검색 서비스 (제휴 병원 및 검진 이벤트)<br />
2. 혜택받기 서비스<br />
3. 상담하기 서비스<br />
4. 게시판형 서비스<br />
5. 건강검진 관련 컨텐츠 제공<br />
6. 위치 관련 서비스<br />
7. 찜하기 서비스<br />
8. 기타 &ldquo;회사&rdquo;가 추가 개발하거나 다른 회사와의 제휴계약 등을 통해 &ldquo;회원&rdquo;에게 제공하는 일체의 서비스<br />
②회사는 &ldquo;서비스&rdquo;를 일정범위로 분할하여 각 범위 별로 이용가능시간을 별도로 지정할 수 있습니다. 다만, 이러한 경우에는 그 내용을 사전에 공지합니다.<br />
③&ldquo;서비스&rdquo;는 연중무휴, 1일 24시간 제공함을 원칙으로 합니다.<br />
④&ldquo;회사&rdquo;는 컴퓨터 등 정보통신설비의 보수점검, 교체 및 고장, 통신두절 또는 운영상 상당한 이유가 있는 경우 &ldquo;서비스&rdquo;의 제공을 일시적으로 중단할 수 있습니다. 이 경우 &ldquo;회사&rdquo;는 제9조 &ldquo;회원&rdquo;에 대한 통지에 정한 방법으로<br />
&ldquo;회원&rdquo;에게 통지합니다. 다만, &ldquo;회사&rdquo;가 사전에 통지할 수 없는 부득이한 사유가 있는 경우 사후에 통지할 수 있습니다.<br />
⑤&ldquo;회사&rdquo;는 서비스의 제공에 필요한 경우 정기점검을 실시할 수 있으며, 정기점검시간은 서비스제공화면에 공지한 바에 따릅니다.</p>

<p>제 13 조 (&ldquo;서비스&rdquo;의 변경)</p>

<p>①&ldquo;회사&rdquo;는 상당한 이유가 있는 경우에 운영상, 기술상의 필요에 따라 제공하고 있는 전부 또는 일부 &ldquo;서비스&rdquo;를 변경할 수 있습니다.<br />
②&ldquo;서비스&rdquo;의 내용, 이용방법, 이용시간에 대하여 변경이 있는 경우에는 변경사유, 변경될 서비스의 내용 및 제공일자 등은 그 변경 전에 해당 서비스 초기화면에 게시하여야 합니다.<br />
③&ldquo;회사&rdquo;는 무료로 제공되는 서비스의 일부 또는 전부를 회사의 정책 및 운영의 필요상 수정, 중단, 변경할 수 있으며, 이에 대하여 관련법에 특별한 규정이 없는 한 &ldquo;회원&rdquo;에게 별도의 보상을 하지 않습니다.</p>

<p>제 14 조 (정보의 제공 및 광고의 게재)</p>

<p>①&ldquo;회사&rdquo;는 &ldquo;회원&rdquo;이 &ldquo;서비스&rdquo; 이용 중 필요하다고 인정되는 다양한 정보를 공지사항이나 전자우편 등의 방법으로 &ldquo;회원&rdquo;에게 제공할 수 있습니다. 다만, &ldquo;회원&rdquo;은 관련법에 따른 거래관련 정보 및 고객문의 등에 대한 답변 등을<br />
제외하고는 언제든지 전자우편에 대해서 수신 거절을 할 수 있습니다.<br />
②제1항의 정보를 전화 및 모사전송기기에 의하여 전송하려고 하는 경우에는 &ldquo;회원&rdquo;의 사전 동의를 받아서 전송합니다. 다만, &ldquo;회원&rdquo;의 거래관련 정보, 상담하기, 혜택받기 서비스로 인한 이메일 발송 및 문자, 통화와 관련된 내<br />
용은 제외됩니다.<br />
③&ldquo;회사&rdquo;는 &ldquo;서비스&rdquo;의 운영과 관련하여 서비스 화면, 홈페이지, 어플리케이션, 전자우편 등에 광고를 게재할 수 있습니다. 광고가 게재된 전자우편을 수신한 &ldquo;회원&rdquo;은 수신거절을 &ldquo;회사&rdquo;에게 할 수 있습니다.<br />
④&ldquo;이용자(회원, 비회원 포함)&rdquo;는 회사가 제공하는 서비스와 관련하여 게시물 또는 기타 정보를 변경, 수정, 제한하는 등의 조치를 취하지 않습니다.</p>

<p>제 15 조 (&ldquo;게시물&rdquo;의 저작권)</p>

<p>①&ldquo;회원&rdquo;이 &ldquo;서비스&rdquo; 내에 게시한 &ldquo;게시물&rdquo;의 저작권은 해당 게시물의 저작자에게 귀속됩니다.<br />
②&ldquo;회원&rdquo;이 &ldquo;서비스&rdquo; 내에 게시하는 &ldquo;게시물&rdquo;은 검색결과 내지 &ldquo;서비스&rdquo; 및 관련 프로모션 등에 노출될 수 있으며, 해당 노출을 위해 필요한 범위 내에서는 일부 수정, 복제, 편집되어 게시될 수 있습니다. 이 경우, 회사는 저<br />
작권법 규정을 준수하며, &ldquo;회원&rdquo;은 언제든지 고객센터 또는 &ldquo;서비스&rdquo; 내 관리기능을 통해 해당 게시물에 대해 삭제, 검색결과 제외, 비공개 등의 조치를 취할 수 있습니다.<br />
③&ldquo;회사&rdquo;는 제2항 이외의 방법으로 &ldquo;회원&rdquo;의 &ldquo;게시물&rdquo;을 이용하고자 하는 경우에는 전화, 팩스, 전자우편 등을 통해 사전에 &ldquo;회원&rdquo;의 동의를 얻어야 합니다.</p>

<p>제 16 조 (&ldquo;게시물&rdquo;의 관리)</p>

<p>①&ldquo;회원&rdquo;의 &ldquo;게시물&rdquo;이 &ldquo;정보통신망법&rdquo; 및 &ldquo;저작권법&rdquo;등 관련법에 위반되는 내용을 포함하는 경우, 권리자는 관련법이 정한 절차에 따라 해당 &ldquo;게시물&rdquo;의 게시중단 및 삭제 등을 요청할 수 있으며, &ldquo;회사&rdquo;는 관련법에 따라 조치<br />
를 취하여야 합니다.<br />
②&ldquo;회사&rdquo;는 전항에 따른 권리자의 요청이 없는 경우라도 아래 규정에서와 같이 권리침해가 인정될 만한 사유가 있거나 기타 회사 정책 및 관련법에 위반되는 경우에는 관련법에 따라 해당 &ldquo;게시물&rdquo;에 대해 임시조치 등을 취할 수<br />
있습니다.<br />
&ndash; 다른 회원 또는 제 3자에게 심한 모욕을 주거나 명예를 손상시키는 내용인 경우<br />
&ndash; 공공질서 및 미풍양속에 위반되는 내용을 유포하거나 링크시키는 경우<br />
&ndash; 불법복제 또는 해킹을 조장하는 내용인 경우<br />
&ndash; 영리를 목적으로 하는 광고일 경우<br />
&ndash; 범죄와 결부된다고 객관적으로 인정되는 내용일 경우<br />
&ndash; 다른 이용자 또는 제 3자의 저작권 등 기타 권리를 침해하는 내용인 경우<br />
&ndash; 사적인 정치적 판단이나 종교적 견해의 내용으로 회사가 서비스 성격에 부합하지 않는다고 판단하는 경우<br />
&ndash; 타인의 건강을 위해 하는 검증되지 않은 허위정보로 판단될 경우<br />
&ndash; 교육인 및 국내 교육기관 간의 비교 광고 성격으로 판단될 경우<br />
&ndash; 의료인 및 국내 의료기관 간의 비교 광고 성격으로 판단될 경우<br />
&ndash; 허가 받지 않은 의료행위 또는 의료행위를 위한 선전으로 게시물을 게재할 경우<br />
&ndash; 회사에서 규정한 게시물 원칙에 어긋나거나, 게시판 성격에 부합하지 않는 경우<br />
&ndash; 기타 관계법령에 위배된다고 판단되는 경우</p>

<p>③본 조에 따른 세부절차는 &ldquo;정보통신망법&rdquo; 및 &ldquo;저작권법&rdquo;이 규정한 범위 내에서 &ldquo;회사&rdquo;가 정한 &ldquo;게시중단요청서비스&rdquo;에 따릅니다.</p>

<p>제 17 조 (권리의 귀속)</p>

<p>①&ldquo;서비스&rdquo;에 대한 저작권 및 지적재산권은 &ldquo;회사&rdquo;에 귀속됩니다. 단, &ldquo;회원&rdquo;의 &ldquo;게시물&rdquo; 및 제휴계약에 따라 제공된 저작물 등은 제외합니다.<br />
②&ldquo;회사&rdquo;는 서비스와 관련하여 &ldquo;회원&rdquo;에게 &ldquo;회사&rdquo;가 정한 이용조건에 따라 계정, &ldquo;아이디&rdquo;, 콘텐츠 등을 이용할 수 있는 이용권만을 부여하며, &ldquo;회원&rdquo;은 이를 양도, 판매, 담보제공 등의 처분행위를 할 수 없습니다.</p>

<p>제 18 조 (계약해제, 해지 등) ①&ldquo;회원&rdquo;은 언제든지 서비스초기화면의 고객센터 또는 내 정보 관리 메뉴 등을 통하여 이용계약 해지 신청을 할 수 있으며, &ldquo;회사&rdquo;는 관련법 등이 정하는 바에 따라 이를 즉시 처리하여야 합니다.<br />
②&ldquo;회원&rdquo;이 계약을 해지할 경우, 관련법 및 개인정보취급방침에 따라 &ldquo;회사&rdquo;가 회원정보를 보유하는 경우를 제외하고는 해지 즉시 &ldquo;회원&rdquo;의 모든 데이터는 소멸됩니다.</p>

<p>제 19 조 (손해배상)</p>

<p>①회사와 이용자는 서비스 이용과 관련하여 고의 또는 과실로 상대방에게 손해를 끼친 경우에는 이를 배상하여야 합니다.<br />
②단, 회사는 무료로 제공하는 서비스의 이용과 관련하여 개인정보보호정책에서 정하는 내용에 위반하지 않는 한 어떠한 손해도 책임을 지지 않습니다.</p>

<p>제 20 조 (이용제한 등)</p>

<p>①&ldquo;회사&rdquo;는 &ldquo;회원&rdquo;이 이 약관의 의무를 위반하거나 &ldquo;서비스&rdquo;의 정상적인 운영을 방해한 경우, 경고, 일시정지, 영구이용정지 등으로 &ldquo;서비스&rdquo; 이용을 단계적으로 제한할 수 있습니다.<br />
②&ldquo;회사&rdquo;는 전항에도 불구하고, &ldquo;주민등록법&rdquo;을 위반한 명의도용 및 결제도용, &ldquo;저작권법&rdquo; 및 &ldquo;컴퓨터프로그램보호법&rdquo;을 위반한 불법프로그램의 제공 및 운영방해, &ldquo;정보통신망법&rdquo;을 위반한 불법통신 및 해킹, 악성프로그램의 배<br />
포, 접속권한 초과행위 등과 같이 관련법을 위반한 경우에는 즉시 영구이용정지를 할 수 있습니다. 본 항에 따른 영구이용정지 시 &ldquo;서비스&rdquo; 이용을 통해 획득한 혜택 등도 모두 소멸되며, &ldquo;회사&rdquo;는 이에 대해 별도로 보상하지 않<br />
습니다.<br />
④&ldquo;회사&rdquo;는 본 조의 이용제한 범위 내에서 제한의 조건 및 세부내용은 이용제한정책 및 개별 서비스상의 운영정책에서 정하는 바에 의합니다.<br />
⑤본 조에 따라 &ldquo;서비스&rdquo; 이용을 제한하거나 계약을 해지하는 경우에는 &ldquo;회사&rdquo;는 제9조 &ldquo;회원&rdquo;에 대한 통지에 따라 통지합니다.<br />
⑥&ldquo;회원&rdquo;은 본 조에 따른 이용제한 등에 대해 &ldquo;회사&rdquo;가 정한 절차에 따라 이의신청을 할 수 있습니다. 이 때 이의가 정당하다고 &ldquo;회사&rdquo;가 인정하는 경우 &ldquo;회사&rdquo;는 즉시 &ldquo;서비스&rdquo;의 이용을 재개합니다.</p>

<p>제 21 조 (책임제한)</p>

<p>①&ldquo;회사&rdquo;는 컴퓨터등 정보통신설비의 보수점검,교체 및 고장, 통신의 두절&nbsp; 의 사유가 발생한 경우에는 서비스의 제공을 일시적으로 중단할 수 있습니다.<br />
②&ldquo;회사&rdquo;는 천재지변 또는 이에 준하는 불가항력으로 인하여 &ldquo;서비스&rdquo;를 제공할 수 없는 경우에는 &ldquo;서비스&rdquo; 제공에 관한 책임이 면제됩니다.<br />
③&ldquo;회사&rdquo;는 &ldquo;회원&rdquo;의 귀책사유로 인한 &ldquo;서비스&rdquo; 이용의 장애에 대하여는 책임을 지지 않습니다.<br />
④&ldquo;회사&rdquo;는 &ldquo;회원&rdquo;이 &ldquo;서비스&rdquo;와 관련하여 게재한 정보, 자료, 사실의 신뢰도, 정확성 등의 내용에 관하여는 책임을 지지 않습니다.<br />
⑤&ldquo;회사&rdquo;는 &ldquo;회원&rdquo; 간 또는 &ldquo;회원&rdquo;과 제3자 상호간에 &ldquo;서비스&rdquo;를 매개로 하여 거래 등을 한 경우에는 책임이 면제됩니다.<br />
⑥&ldquo;회사&rdquo;는 무료로 제공되는 서비스 이용과 관련하여 관련법에 특별한 규정이 없는 한 책임을 지지 않습니다.</p>

<p>제 22 조 (준거법 및 재판관할)</p>

<p>①&ldquo;회사&rdquo;와 &ldquo;회원&rdquo; 간 제기된 소송은 대한민국법을 준거법으로 합니다.<br />
②&ldquo;회사&rdquo;와 &ldquo;회원&rdquo;간 발생한 분쟁에 관한 소송은 제소 당시의 &ldquo;회원&rdquo;의 주소에 의하고, 주소가 없는 경우 거소를 관할하는 지방법원의 전속관할로 합니다. 단, 제소 당시 &ldquo;회원&rdquo;의 주소 또는 거소가 명확하지 아니한 경우의 관할<br />
법원은 민사소송법에 따라 정합니다.</p>

